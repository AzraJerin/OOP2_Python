# Importing random Module  
import random  
  
# Creating a list  
my_list = [1, 2, 3, 4, 5]  
print("Before Shuffling:", my_list)  
  
# Shuffle the list  
random.shuffle(my_list)  
print("After first Shuffling:", my_list)  
  
# Shuffle the list again  
random.shuffle(my_list)  
print("After second Shuffling:", my_list)  